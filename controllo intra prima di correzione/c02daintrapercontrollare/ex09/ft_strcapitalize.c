char	*ft_strcapitalize(char *str)
{
	unsigned int	i;
	int	p;

	i = 0;
	p = 1;

	while (str[i] != '\0')
	{
		char c = str[i];
		
		if (p == 1 && (c >= 'a' && c <= 'z'))
		{
			str[i] = c - 32;
		}
		else if (p == 0 && (c >= 'A' && c <= 'Z'))
		{
			str[i] = c + 32;
		}
		if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9'))
		{
			p = 0;
		}
		else 
		{
			p = 1;
		}
		i++;
	}
	return (str);
}
