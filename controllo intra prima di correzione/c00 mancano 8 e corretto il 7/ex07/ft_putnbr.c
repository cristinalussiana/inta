#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	n;

	n = 0;
	if (nb == -2147483648)
	{
		write (1, "-", 1);
		write (1, "2147483648", 10);
	}
	else if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
		ft_putnbr();
	}
	else if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb < 10)
	{
		n = nb + 48;
		write (1, &n, 1);
	}
}
